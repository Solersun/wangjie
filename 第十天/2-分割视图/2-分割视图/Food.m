- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Jane"];
    if (!cell) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:@"Jane"];
    }
    cell.textLabel.text = [NSString stringWithFormat:@"第%ld道菜",indexPath.row];
    return cell;
}


-(void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {

    if (_delegate &&[_delegate respondsToSelector:@selector(didSelectRow:)]) {
        [_delegate didSelectRow:indexPath.row];
    }
}

